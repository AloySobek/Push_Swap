#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int count;

	count = 0;
	while (s1[count] != '\0')
		count++;
	while (*s2 != '\0')
		s1[count++] = *s2++;
	s1[count] = '\0';
	return (s1);
}
