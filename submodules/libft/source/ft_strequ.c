#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int res;

	res = 0;
	if (s1 && s2)
	{
		res = ft_strcmp(s1, s2);
		if (res == 0)
			return (1);
	}
	return (0);
}
